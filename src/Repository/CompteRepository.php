    public function GetPrivilege($login, $password)
    {
        $entityManager =$this->getEntityManager();
        $query = $entityManager->createQuery(
                    'SELECT  c.privilege , c.idf , c.idsoc
                    FROM App\Entity\Compte c , App\Entity\Freelance f , App\Entity\Societe s
                    WHERE c.login= :login AND c.password = :password 
                    GROUP BY c.idf , c.idsoc
                    '
                    )->setParameter('login', $login)
                    ->setParameter('password', $password);
        return $query->getResult();
        
    }
